package com.ldapui.model;

/**
 * This represents a user role.
 * 
 * @author George
 * 
 */
public class Role {

	String id;
	String name;

	// //////////// GETTERS AND SETTERS ////////////////

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
